package model;

public class PupukModel {
    private int idPupuk;
    private String namaPupuk;
    private double hargaPupuk;

    // Getters and Setters
    public int getIdPupuk() {
        return idPupuk;
    }

    public void setIdPupuk(int idPupuk) {
        this.idPupuk = idPupuk;
    }

    public String getNamaPupuk() {
        return namaPupuk;
    }

    public void setNamaPupuk(String namaPupuk) {
        this.namaPupuk = namaPupuk;
    }

    public double getHargaPupuk() {
        return hargaPupuk;
    }

    public void setHargaPupuk(double hargaPupuk) {
        this.hargaPupuk = hargaPupuk;
    }
}
